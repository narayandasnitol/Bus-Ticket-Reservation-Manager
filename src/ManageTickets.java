    public void getData(){
        ResultSet resultSet = null;
        try{
            String query = "SELECT * FROM ticket";
            Statement statement ;
            statement = connect.createStatement();
            resultSet = statement.executeQuery(query);
            while(resultSet.next()){
                int ticketID = resultSet.getInt("ticketID");
                arrID.add(resultSet.getInt("ticketID"));
                int numberofTickets=resultSet.getInt("numberofTickets");
                String seatType=resultSet.getString("seatType");
                String journeyDate=resultSet.getDate("journeyDate").toString();
                Double price=resultSet.getDouble("price");
                String seatNumber=resultSet.getString("seatNumber");
                String issueDate=resultSet.getDate("issueDate").toString();
                String routeNumber=resultSet.getString("route");
                String departureTime=resultSet.getTime("departureTime").toString();
                String payment=resultSet.getString("paymentType");
                Object row[]={ticketID,routeNumber,issueDate,journeyDate,seatType,seatNumber,departureTime,numberofTickets,price,payment};
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.addRow(row);
            }
        }catch(Exception e){
            System.out.println("Error in Query..");
            e.printStackTrace();
        }           
    }

