    public void getData(){
        ResultSet resultSet = null;
        try{
            String query = "SELECT * FROM ticket WHERE ticketID="+id;
            Statement statement ;
            statement = connect.createStatement();
            resultSet = statement.executeQuery(query);
            if(resultSet.next()){
                jTextField1.setText(resultSet.getInt("passengerID")+"");
                jTextField3.setText(resultSet.getString("seatNumber"));
                jTextField5.setText(resultSet.getString("routeNumber"));
                jTextField8.setText(resultSet.getDouble("price")+"");
                if(resultSet.getString("seatType").toLowerCase().equals("classic")){
                    jComboBox1.setSelectedIndex(1);
                }else if(resultSet.getString("seatType").toLowerCase().equals("economy")){
                    jComboBox1.setSelectedIndex(2);
                }
                jDateChooser1.setDate(resultSet.getDate("issueDate"));
                jDateChooser2.setDate(resultSet.getDate("journeyDate"));
                timePicker1.setText(resultSet.getTime("departureTime").toString());
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }

