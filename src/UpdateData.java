    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String a="",b="",c="",d="",e="",f="",g="",h="";
        a=jTextField1.getText().toString();
        b=jComboBox1.getSelectedItem().toString();
        c=jTextField3.getText().toString();
        d= ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();
        e=((JTextField)jDateChooser2.getDateEditor().getUiComponent()).getText();
        if(timePicker1.getText().contains("am")){
            String arr[]=timePicker1.getText().split(":");
            f=arr[0]+":"+(arr[1].replace("am",""))+":"+"00";
        }else if(timePicker1.getText().contains("pm")){
            String arr[]=timePicker1.getText().split(":");
            f=(12+Integer.parseInt(arr[0]))+":"+(arr[1].replace("pm",""))+":"+"00";
        }
        g=jTextField5.getText().toString();
        h=jTextField8.getText().toString();
        if(a.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter passenger id");
        }else if(b.equals("Choose Seat Type")){
            JOptionPane.showMessageDialog(null, "Please select seat type");
        }else if(c.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter seat number");
        }else if(d.equals("")){
            JOptionPane.showMessageDialog(null, "Please select issue date");
        }else if(e.equals("")){
            JOptionPane.showMessageDialog(null, "Please select journey date");
        }else if(f.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter departure time");
        }else if(g.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter route number");
        }else if(h.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter price");
        }else{
            try{
                PreparedStatement ps = connect.prepareStatement(
      "UPDATE ticket SET passengerID = ?, seatType = ?,seatNumber=?,issueDate=?,journeyDate=?,departureTime=?,routeNumber=?,price=? WHERE ticketID = ?");
                ps.setString(1,a);
                ps.setString(2,b);
                ps.setString(3,c);
                ps.setString(4,d);
                ps.setString(5,e);
                ps.setString(6,f);
                ps.setString(7,g);
                ps.setString(8,h);
                ps.setInt(9, id);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data updated successfully");
                ps.close();
                ManageTickets ob=new ManageTickets();
                ob.setVisible(true);
                dispose();
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, "Cannot update in the database");
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

