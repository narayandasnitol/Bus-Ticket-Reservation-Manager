    private void jLabel8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel8MouseClicked
        // TODO add your handling code here:
        String b=jTextField1.getText().toString();
        if(b.equals("")){
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            jTable1.revalidate();
            getData();
        }else{
            Integer in=Integer.parseInt(b);
            String query = "SELECT * FROM ticket where ticketID = "+in;
            Statement statement ;
            try {
                statement = connect.createStatement();
                ResultSet set = statement.executeQuery(query);
                if(set.next()){
                    int ticketID = set.getInt("ticketID");
                    arrID.add(set.getInt("ticketID"));
                    int numberofTickets=set.getInt("numberofTickets");
                    String seatType=set.getString("seatType");
                    String journeyDate=set.getDate("journeyDate").toString();
                    Double price=set.getDouble("price");
                    String seatNumber=set.getString("seatNumber");
                    String issueDate=set.getDate("issueDate").toString();
                    String routeNumber=set.getString("route");
                    String departureTime=set.getTime("departureTime").toString();
                    String payment=set.getString("paymentType");
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    Object row[]={ticketID,routeNumber,issueDate,journeyDate,seatType,seatNumber,departureTime,numberofTickets,price,payment};
                    model.setRowCount(0);
                    jTable1.revalidate();
                    model.addRow(row);
                }
            } catch (SQLException ex) {
                Logger.getLogger(ManageTickets.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jLabel8MouseClicked

